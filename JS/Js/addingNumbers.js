//Adding Numbers
var sum = 8 + 5;
// Displaying the result
console.log("The sum of 8 + 5 is: " + sum);

//Subtracting Numbers
var difference = 10 - 3;
// Displaying the result
console.log("The difference of 10 - 3 is: " + difference);

//Multiplying Numbers
var product = 4 * 7;
// Displaying the result
console.log("The product of 4 * 7 is: " + product);

//Dividing Numbers
var quotient = 20 / 4; 
// Displaying the result
console.log("The quotient of 20 / 4 is: " + quotient);

// Incrementing Numbers
var increment = 5;
increment = 5 + 1; // Incrementing by 1
// Alternatively, you can use increment operator
console.log("The incremented value of 5 is: " + increment);
increment++;
// Displaying the incremented value
console.log("The incremented value of 6 is: " + increment);

// Decrementing Numbers
var decrement = 10;
decrement = 10 - 1; // Decrementing by 1
// Alternatively, you can use decrement operator
console.log("The decremented value of 10 is: " + decrement);
decrement--;
// Displaying the decremented value
console.log("The decremented value of 9 is: " + decrement);

// Decimal Numbers
var decimalSum = 2.5 + 3.5;
// Displaying the result
console.log("The sum of 2.5 + 3.5 is: " + decimalSum);

// Multiply Decimals
var decimalProduct = 1.2 * 3.4;
// Displaying the result
console.log("The product of 1.2 * 3.4 is: " + decimalProduct);

// Division with Decimals
var decimalQuotient = 7.5 / 2.5;
// Displaying the result
console.log("The quotient of 7.5 / 2.5 is: " + decimalQuotient);

// Finding a Remainder
var remainder = 10 % 3;
// Displaying the result
console.log("The remainder of 10 % 3 is: " + remainder);

// Compound Assignment with Augmented Addition
var compoundSum = 5;
compoundSum += 3; // Equivalent to compoundSum = compoundSum + 3
// Displaying the result
console.log("The compound sum after adding 3 is: " + compoundSum);

// Compound Assignment with Augmented Subtraction
var compoundDifference = 10;
compoundDifference -= 4; // Equivalent to compoundDifference = compoundDifference - 4
// Displaying the result
console.log("The compound difference after subtracting 4 is: " + compoundDifference);

// Compound Assignment with Augmented Multiplication
