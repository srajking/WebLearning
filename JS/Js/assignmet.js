/*
    هناك فرق بين الاعلان عن متغير
    و بين تعيين قيمة له
    في المثال التالي تم الاعلان عن متغير a
    و لم يتم تعيين قيمة له
    بينما تم الاعلان عن متغير b
    و تعيين قيمة له
    و بالتالي المتغير a سيكون undefined
    //    و المتغير b سيكون 10
*/

var a;

console.log(a); // undefined

var b = 10;
console.log(b); // 10

a = 20; // تعيين قيمة للمتغير a
console.log(a); // 20

b = a; // تعيين قيمة المتغير a للمتغير b
console.log(b); // 20
